import type { AgentMode } from './mode-policy'

/** Beast-автономность — для режимов, где агент работает до результата сам. */
export const BEAST_PRESET = [
  '<!-- beast_mode -->',
  '## Режим автономности',
  'Доводи задачу до полного решения, прежде чем возвращать ход пользователю. НЕ останавливайся на полпути и не спрашивай «продолжать?» на очевидных шагах.',
  'Проверяй свой результат жёстко: после правок прогоняй проверки (тесты/typecheck/линт), не assume команды — найди их в README/package.json. Недостаточная проверка — главная причина провала.',
  'Если проверка упала — чини и перепроверяй, не объявляй «готово» раньше времени. Заверши ход только когда задача действительно решена; если упёрся в стену — скажи ТОЧНО, что блокирует, без воды.',
].join('\n')

/** Пресет под режим агента. Beast — для автономных (auto/bypass), иначе пусто. */
export function buildModePreset(agentMode: AgentMode): string {
  if (agentMode === 'auto' || agentMode === 'bypass') return BEAST_PRESET
  return ''
}

