/** Corrective-nudge: модель ответила прозой и не вызвала ни одного инструмента,
 *  хотя задача агентная. Один раз просим либо явно завершить, либо вызвать тул. */
export const IGNORED_TOOLS_NUDGE = [
  'Ты не вызвал ни одного инструмента и ответил только текстом.',
  'Если задача ВЫПОЛНЕНА — подтверди это одной короткой строкой.',
  'Если НЕТ — вызови нужный инструмент (read_file / write_file / apply_patch / run_command и т.п.), а не описывай действие словами.',
].join('\n')

// JS `\b` — ASCII-only word boundary: `\bготово\b` НИКОГДА не матчит кириллицу (все
// символы «не-\w» → границы нет). Поэтому для русских маркеров строим Unicode-границу
// слова через \p{L}\p{N} (нужен флаг 'u'); английские `\b`-литералы оставляем как есть.
const UWB = '(?<![\\p{L}\\p{N}_])'
const UWE = '(?![\\p{L}\\p{N}_])'
/** Слово(а) с Unicode-границей — работает для кириллицы (в отличие от `\b`). */
const w = (alternation: string): RegExp => new RegExp(UWB + '(?:' + alternation + ')' + UWE, 'iu')

/**
 * Маркеры «модель ПРЕТЕНДУЕТ на выполненное действие» — прошедшее время / завершённость /
 * пассив. Ровно симптом DeepSeek-цикла: «Задача выполнена / я создал файл», а инструмент не
 * вызван. НАМЕРЕННО без инфинитивов («создать», «изменить») и без будущего времени («создам»,
 * «прочитаю») — они появляются в оффере/объяснении/описании процесса («могу создать файл»,
 * «чтобы изменить», «расскажи как ты работаешь» → «я прочитаю запрос…») и дали бы ложный nudge.
 */
const ACTION_CLAIM_MARKERS: RegExp[] = [
  // — Завершение задачи (высокоточные) —
  /(?<![\p{L}\p{N}_])задач[аи]\s+(выполнен|завершён|завершен|решён|решен|готов)/iu,
  w('готово|готова|выполнено|выполнена|выполнил|выполнила|выполнили|сделано|завершено|завершена'),
  w('task\\s+(is\\s+)?(complete|completed|done|finished)'),
  w('all\\s+(done|set)'),
  // — Действие над файлом/кодом: прошедшее время / пассив (ru). Инфинитив/будущее — НЕ здесь. —
  w('созда(л|ла|ли|н|на|но)'),
  w('измени(л|ла|ли)|изменён|изменен|изменено'),
  w('обнови(л|ла|ли)|обновлён|обновлен|обновлено'),
  w('удали(л|ла|ли)|удалён|удален|удалено'),
  w('добави(л|ла|ли)|добавлен|добавлено'),
  // Только активное «написал»: пассив «написан/написано» двусмыслен и частотен в разговоре
  // («там написано, что…», «написан на TypeScript», «в доке написано») → ложный nudge.
  w('написа(л|ла|ли)'),
  w('примени(л|ла|ли)|применён|применен'),
  w('исправи(л|ла|ли)|исправлен|исправлено'),
  w('замени(л|ла|ли)|заменён|заменено'),
  w('переписа(л|ла|ли)|реализова(л|ла|ло|но)'),
  w('внёс|внес'),
  // Частотные перфективы config/run/build-задач — ТОЛЬКО активное прошедшее. Пассивы
  // (настроен/запущен/собран/установлен/проверен) и «проверил» осознанно НЕ здесь: они
  // двусмысленны в разговоре («я настроен помогать», «проверен временем», «я проверил
  // документацию») → ложный nudge (то же правило, что для en made/ran/set).
  w('настрои(л|ла|ли)|запусти(л|ла|ли)|собра(л|ла|ли)|установи(л|ла|ли)|сохрани(л|ла|ли)|поправи(л|ла|ли)'),
  // — EN: прошедшее время 1-го лица / презентация результата. made/ran/set НЕ добавлены
  //   осознанно — двусмысленны («I made a mistake», «I ran into an issue») → ложный nudge. —
  /\bi(?:['’]ve| have)?\s+(created|modified|updated|deleted|removed|added|wrote|written|applied|fixed|replaced|changed|implemented|refactored|completed|finished)\b/i,
  /\b(created|updated|modified|deleted|applied|added|wrote|written)\s+(the\s+|a\s+|an\s+|your\s+)?(file|patch|function|code|method|class|test|config|module)\b/i,
  /\bhere(?:['’]s| is)\s+the\s+(updated|modified|new|fixed|corrected|complete)\b/i,
  /\b(file|patch|change|code)\s+(is\s+now|has\s+been)\s+(created|updated|modified|fixed|applied|written)\b/i,
  // — Диф / код-блок с путём («вот результат», не разговор) —
  /```diff/i,
  /```[a-z0-9]*\s*\n\s*(?:\/\/|#)\s*[\w./-]+\.\w{1,6}\b/i,
]

/**
 * Контексты, в которых маркер — НЕ претензия, а оффер / условие / гипотеза / инструкция /
 * САМООПИСАНИЕ (про себя/продукт, не про правку файла). Проверяются ПОКЛАУЗНО (в той же клаузе,
 * что и маркер), поэтому «Задача выполнена. Если хочешь, могу ещё» остаётся претензией.
 */
const NON_CLAIM_GUARDS: RegExp[] = [
  w('могу|можешь|можете|можно|мог|могла|могли'),
  w('если'),
  w('бы'),
  w('чтобы'),
  w('нужно|надо|стоит|следует|давай|давайте'),
  w('предлагаю|рекомендую|советую'),
  w('хочешь|хотите|хотел|хотела'),
  // Временно́е/итеративное придаточное В НАЧАЛЕ клаузы = описание процесса, не факт («когда
  // задача выполнена, я …» на «как ты работаешь»). Только clause-start: хвостовой адъюнкт
  // («я создал файл, когда закончил») — реальная претензия, его не гасим.
  /^\s*(?:когда|пока|как\s+только|после\s+того|каждый\s+раз|всякий\s+раз|обычно|всегда)(?![\p{L}\p{N}_])/iu,
  // Самоописание ассистента: «я создан…», «меня создали…» ≠ «я создал файл».
  /(?<![\p{L}\p{N}_])(я\s+созда(н|на|но)|меня\s+созда(л|ли|н|на))/iu,
  // Перечень возможностей ПРОДУКТА: «реализовано 18 провайдеров», «добавлено 8 коннекторов».
  // Только реализова/добавл + число + продуктовое существительное — «создано 2 файла» /
  // «обновлено 5 строк» (отчёт о правке) под guard НЕ попадают, остаются претензией.
  /(?<![\p{L}\p{N}_])(реализова(?:но|н|на|ны|ло)|добавл(?:ено|ен|ена|ены))\s+(?:\d+|несколько|множество|ряд|немало|много)\s+(?:\p{L}+\s+){0,2}?(провайдер|коннектор|модел|режим|интеграц|инструмент|фич|виджет|формат|язык|таблиц)/iu,
  // Дативный получатель/безличность: «мне добавили доступ» ≠ агент правки. НЕ гасим, если
  // подлежащее — сам ассистент («я вам обновил конфиг» = претензия).
  /(?<![\p{L}\p{N}_])(?<!я\s)(мне|нам|тебе|вам|нас|ему|ей|им)\s+(?:\S+\s+){0,2}?(добави|обнови|измени|удали|созда|написа|реализова|примени|настрои)/iu,
  /\bi\s+(can|could|would|will|might)\b/i,
  /\bi['’]ll\b/i,
  /\bif\s+you\b/i,
  /\b(to|should|can|could|would|need\s+to|want\s+to)\s+(create|update|modify|delete|add|write|apply|change|fix|implement)\b/i,
  /\byou\s+(should|can|could|might|need|may)\b/i,
]

/**
 * true — текст модели претендует на ВЫПОЛНЕННОЕ действие без вызова инструмента (симптом,
 * ради которого существует corrective-nudge). false — чистая проза: объяснение, список
 * шагов, вопрос, оффер, гипотеза. Дискриминатор по ВЫХЛОПУ модели (не по словам юзера) —
 * гейтит nudge внутри агентных режимов (auto/bypass/accept-edits), где сам режим не является
 * сигналом «это агентная задача» (Павел повседневно живёт в 'auto').
 *
 * Поклаузная логика: claim засчитывается, только если в ЭТОЙ ЖЕ клаузе нет guard-маркера.
 */
export function claimsCompletedAction(text?: string): boolean {
  const raw = text ?? ''
  if (!raw.trim()) return false
  const lower = raw.toLowerCase()
  // Диф / код-блок с путём проверяем по всему тексту (переносы строк — часть паттерна).
  if (/```diff/.test(lower)) return true
  if (/```[a-z0-9]*\s*\n\s*(?:\/\/|#)\s*[\w./-]+\.\w{1,6}\b/.test(lower)) return true
  // Одиночная EN-реплика-завершение «Done.»/«All done» — проверяем ВЕСЬ текст целиком (не
  // поклаузно): иначе «done» как элемент перечисления («states: pending, done, failed»)
  // ложно сработал бы. Якорь ^…$ отсекает «I'm not done yet» / «are you done?».
  if (/^\s*(all\s+)?(done|finished|complete|completed)[\s.!]*$/.test(lower)) return true
  // Запятая — тоже граница клаузы: «обновил конфиг, чтобы включить логи» = claim(«обновил
  // конфиг») + отдельный guard(«чтобы …»), а не гашение claim'а следующей целью/шагом.
  const clauses = lower.split(/[.!?\n;,]+/)
  for (const clause of clauses) {
    if (!clause.trim()) continue
    if (ACTION_CLAIM_MARKERS.some(re => re.test(clause)) && !NON_CLAIM_GUARDS.some(re => re.test(clause))) {
      return true
    }
  }
  return false
}
