const DEFAULT_SCRIPTS = Object.freeze({
  type: 'node verify.mjs',
  'test:fast': 'node verify.mjs',
  test: 'node verify.mjs',
})

function scenario(config) {
  const protectedFile = config.protectedFile ?? 'protected.txt'
  const protectedContent = config.protectedContent ?? 'do-not-touch\n'
  return {
    id: config.id,
    fixtureVersion: 1,
    suite: 'core',
    category: config.category,
    recipe: config.recipe ?? 'small-edit',
    maxTurns: config.maxTurns ?? 9,
    expectedFiles: [config.target],
    unrelatedFiles: [protectedFile],
    verify: ['npm run test:fast'],
    requiresReview: config.requiresReview ?? false,
    task: `${config.task} Do not touch ${protectedFile}. Run npm run test:fast${config.requiresReview ? ' and pass review_before_commit' : ''}.`,
    scripts: DEFAULT_SCRIPTS,
    files: {
      [config.target]: config.before,
      [protectedFile]: protectedContent,
      'verify.mjs': verifier(config.target, config.after, protectedFile, protectedContent),
      ...(config.extraFiles ?? {}),
    },
    ...(config.afterBaselineFiles ? { afterBaselineFiles: config.afterBaselineFiles } : {}),
  }
}

function verifier(target, expected, protectedFile, protectedContent) {
  return [
    "import { readFileSync } from 'node:fs';",
    `if (readFileSync(${JSON.stringify(target)}, 'utf8') !== ${JSON.stringify(expected)}) throw new Error('target does not match deterministic oracle');`,
    `if (readFileSync(${JSON.stringify(protectedFile)}, 'utf8') !== ${JSON.stringify(protectedContent)}) throw new Error('protected file changed');`,
    "console.log('oracle ok');",
    '',
  ].join('\n')
}

