// Хиро-пресеты Scheduler'а — сценарий «Контролируемый AI-дежурный» (1.9.9).
// Превращают пустую NL+prompt форму в готовые продуктовые сценарии в один клик.
//
// ВАЖНО про безопасность: фоновый scheduled-прогон строго READ-ONLY
// (SCHEDULED_READONLY_TOOLS — чтение файлов + читающий connector_query, БЕЗ
// write_file/run_command). Поэтому пресеты НЕ обещают «запусти сборку/тесты» —
// дозор ТОЛЬКО наблюдает (код + данные коннекторов), находит проблему, показывает
// источник и предлагает фикс. Сам фикс (с командами/правками) запускается отдельно,
// под подтверждением, через кнопку «Починить в чате» на результате прогона.
// Все prompt'ы явно требуют: показать источник (файл:строка / коннектор), предложить
// шаги, и НИЧЕГО не менять — это и есть «контроль».

export interface SchedulerPreset {
  id: string
  icon: string
  label: string
  /** Короткое пояснение под чипом — что делает дозор. */
  hint: string
  /** Расписание на естественном языке (поле «Когда?»). */
  nl: string
  /** Что сделать (поле «Что сделать?») — read-only дозор + отчёт + предложение фикса. */
  prompt: string
}

