/**
 * Task-spec контракт (v3, Шаг B). Главный рычаг «Opus-результат на дешёвой модели»:
 * дорогая модель один раз пишет ДЕТАЛЬНЫЕ описания задач, дешёвая исполняет их по
 * одной как точную инструкцию. Вдохновлено планировщиком ClawCode:
 * «Each task description is READ AND EXECUTED BY AN LLM CODING AGENT — not a human.
 *  Exact file paths, acceptance criteria (what done looks like). One concern per task.»
 *
 * Здесь: текст контракта для промпта планировщика + чистый scorer качества описания
 * (можно гейтить/предупреждать о слабых шагах). Без UI/БД — тестируется напрямую.
 */

/** Контракт, инжектится в промпт планировщика (дорогая модель пишет ТЗ-задачи). */
export const TASK_SPEC_CONTRACT = [
  'КОНТРАКТ ЗАДАЧИ — каждый шаг плана исполняет отдельный LLM-агент (возможно дешёвая модель).',
  'Пиши detail каждого шага ДЛЯ АГЕНТА-исполнителя, не для человека:',
  '- Точные файлы/пути, которые создать или изменить.',
  '- Что именно сделать (функции, эндпоинты, поля, компоненты).',
  '- Критерий готовности: что значит «сделано» (как проверить).',
  '- Одна задача = одна забота. Сложное — разбей на отдельные шаги.',
  '- 3–10 конкретных строк на шаг. Без общих фраз «улучшить»/«оптимизировать»/«доработать».',
].join('\n')

export interface TaskSpecScore {
  ok: boolean
  /** Чего не хватает по контракту (для предупреждения/гейта). */
  missing: string[]
}

/**
 * Оценить описание шага против контракта. Эвристика: путь к файлу + критерий
 * готовности + достаточная детальность. Возвращает чего не хватает.
 */
export function scoreTaskSpec(detail: string | null | undefined): TaskSpecScore {
  const d = (detail ?? '').trim()
  const missing: string[] = []

  // 1. Конкретные файлы/пути: что-то вроде foo.ts, src/bar, a/b.css.
  const hasPath = /[\w@.-]+\.[a-z]{1,5}\b/i.test(d) || /\b[\w-]+\/[\w-]+/.test(d)
  if (!hasPath) missing.push('конкретные файлы/пути')

  // 2. Критерий готовности — что значит «сделано».
  const hasAcceptance = /критери|готов|done|acceptance|ожида|проверь|провер|должен|тест|works?|пройд/i.test(d)
  if (!hasAcceptance) missing.push('критерий готовности («сделано» = что)')

  // 3. Детальность — не одна строка-заглушка.
  if (d.length < 40) missing.push('детальность (минимум пара конкретных предложений)')

  return { ok: missing.length === 0, missing }
}
